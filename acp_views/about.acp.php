    <h1>System &raquo; About</h1>
    <p>
      

      <strong>Instaforum</strong> is an instant-deployment fully-integrated web forum solution written in PHP &amp; JS.<br />
      <br />
      
      It comprises a storage engine, forum engine &amp; administration zone in an easy-to-install package.
      <br />
      Instaforum is a dissertation project by Damien Walsh, a third year undergraduate studying Internet Computing at the University of Manchester School of Computer Science.

